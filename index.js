function control(e) {
  switch (e.keyCode) {
    case 40:
      console.log("down");
      break;
    case 38:
      console.log("up");
      break;
    case 37:
      console.log("left");
      break;
    case 39:
      console.log("right");
      break;

    default:
      break;
  }
}
document.addEventListener("keyup", control);
